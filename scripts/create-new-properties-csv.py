def create_csv():
    """Create CSV file with new properties"""
    output_file = 'csv/new-properties/new-glamping-properties.csv'
    
    # Create empty row template with all headers
    def create_empty_row():
        return {header: '' for header in headers}
    
    rows = []
    for prop in properties:
        row = create_empty_row()
        # Fill in known fields
        for key, value in prop.items():
            if key in headers:
                row[key] = value
        rows.append(row)
    
    # Write CSV
    with open(output_file, 'w', encoding='utf-8', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=headers)
        writer.writeheader()
        writer.writerows(rows)
    
    print(f'âœ… Created CSV file: {output_file}')
    print(f'   Added {len(properties)} new glamping properties')
    print('\nProperties included:')
    for prop in properties:
        print(f'  - {prop["Property Name"]} ({prop.get("City", "N/A")}, {prop.get("State", "N/A")})')

if __name__ == '__main__':
    create_csv()
