/** Validate US ZIP: 5 digits or 5+4 format */
export function isValidUsZip(zip: string): boolean {
  const trimmed = zip.trim();
  if (!trimmed) return true; // empty is valid (optional field)
  return /^\d{5}(-\d{4})?$/.test(trimmed);
}
